<?php
$x = 5;

echo ": \$x = $x\n\n";

// 1. Pre-increment
echo "Pre-increment : " . ++$x . "\n";  

// 2. Post-increment
echo "Post-increment: " . $x++ . "\n"; 

// 3. Pre-decrement
echo "Pre-decrement : " . --$x . "\n";  

// 4. Post-decrement
echo "Post-decrement: " . $x-- . "\n"; 

// Final value
echo "\nFinal value of \$x: $x\n";
?>
